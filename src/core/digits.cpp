#include "core/digits.h"

// partial specialization optimization for 32-bit numbers
template<class T>
int digits<T>::num_digits(int32_t x)
{
    if (x == INT_MIN) return 10 + 1;
    if (x < 0) return num_digits(-x) + 1;

    if (x >= 10000) {
        if (x >= 10000000) {
            if (x >= 100000000) {
                if (x >= 1000000000)
                    return 10;
                return 9;
            }
            return 8;
        }
        if (x >= 100000) {
            if (x >= 1000000)
                return 7;
            return 6;
        }
        return 5;
    }
    if (x >= 100) {
        if (x >= 1000)
            return 4;
        return 3;
    }
    if (x >= 10)
        return 2;
    return 1;
}

// partial-specialization optimization for 8-bit numbers
template <class T>
int digits<T>::num_digits(char n)
{
    // if you have the time, replace this with a static initialization to avoid
    // the initial overhead & unnecessary branch
    static char x[256] = {0};
    if (x[0] == 0) {
        for (char c = 1; c != 0; c++)
            x[c] = num_digits((int32_t)c);
        x[0] = 1;
    }
    return x[n];
}